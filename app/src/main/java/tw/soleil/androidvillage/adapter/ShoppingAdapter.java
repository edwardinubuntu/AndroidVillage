package tw.soleil.androidvillage.adapter;

import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;
import android.widget.TextView;
import com.parse.ParseObject;
import tw.soleil.androidvillage.R;

import java.util.List;

/**
 * Created by edward_chiang on 14/12/6.
 */
public class ShoppingAdapter extends ArrayAdapter<ParseObject> {
    /**
     * Constructor
     *
     * @param context  The current context.
     * @param resource The resource ID for a layout file containing a TextView to use when
     *                 instantiating views.
     * @param objects  The objects to represent in the ListView.
     */
    public ShoppingAdapter(Context context, int resource, List<ParseObject> objects) {
        super(context, resource, objects);
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        LayoutInflater inflater = (LayoutInflater)getContext().getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        convertView = inflater.inflate(R.layout.cell_shop_item, null);

        ParseObject eachFoodObject = getItem(position);

        TextView shopItemNameTextView = (TextView) convertView.findViewById(R.id.shop_item_name_text_view);
        shopItemNameTextView.setText(eachFoodObject.getString("name"));

        return convertView;
    }
}
